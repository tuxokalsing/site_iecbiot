								<h3><a id="2014-02-12">Encerramento da XV Maratona de Empreendedorismo</a></h3>
								
								<p>A equipe do IECBiot participou nesta terça feira da cerimônia de encerramento da
								<a class="color1" target="_blank" href="http://www.ufrgs.br/empreendedorismo/maratona-de-empreendedorismo">
								XV Maratona de Empreendimento</a> promovido pela Secretaria de Desenvolvimento Tecnológico (SEDETEC) 
								através do Programa de Empreendedorismo e Inovação. A XV edição da maratona teve a inscrição de 30
								projetos dentre os quais 13 se classificaram como finalistas.</p>
								
								<p>O primeiro colocado foi o projeto 3D Protos Saúde, de autoria de Eloísa Gonzaga, Fernando Flores e Vinícius Amantéa. 
								Eles desenvolveram um processo para confecção de órteses e próteses a partir de escaneamento corporal e posterior impressão 3D. 
								A startup nasceu durante a Maratona. Fernando e Vinícius começaram o curso com uma ideia, abandonada depois que conheceram Eloísa, 
								que também deixou seu projeto inicial de lado para criar a 3D Protos Saúde com os colegas. O segundo colocado foi a startup 
								Love Design Pet, de Renata Moutinho, de mobiliário para cães e gatos. O diferencial do projeto é que o produto tem design 
								que se integra à decoração e é desenvolvido em material ecologicamente correto. Em terceiro lugar ficou a Base Precision, 
								de Jonatan Grandini e Luis Felipe Peixoto. Eles desenvolveram um produto que conjuga hardware e software para avaliar 
								como o surfista usa a prancha. A partir da análise dos dados captados pelo software, os fabricantes podem criar a prancha 
								ideal para cada tipo de cliente.</p>
								
								<p>Todos os finalistas foram premiados com destaque para os 3 finalistas. Os vencedores ganharam, 
								entre outros prêmios, cursos de empreendedorismo e a formalização das empresas. A cerimônia contou 
								com a presença do vice-reitor Rui Vicente Oppermann e da secretária de Desenvolvimento Tecnológico Raquel Mauler, 
								além de integrantes da SEDETEC e das instituições parceiras da UFRGS na Maratona. Oppermann disse que o 
								estímulo ao empreendedorismo tem grande significado social e econômico, pois, segundo ele, 
								“o Brasil precisa ser mais eficiente na transformação da criatividade em projetos que promovam a qualidade de vida”.</p>
								
								<p>Fonte: UFRGS </p>
