								<h3><a id="2015-22-05">XVI Maratona de Empreendedorismo da UFRGS <br> Inscrições abrem nesta segunda-feira 25/05</a></h3>
								<p class="color1 destaque">De 25 de maio a 15 de junho estarão abertas as inscrições para a XVI Maratona de Empreendedorismo da UFRGS. </p>
								<p>A Maratona de Empreendedorismo é um curso de extensão transversal a todas as áreas do conhecimento e aberto
								aos membros das comunidades interna e externas à UFRGS que tenham uma ideia de startup.</p>
								<p>Promovido anualmente pela Secretaria de Desenvolvimento Tecnológico -SEDETEC da UFRGS, através de seu 
								Programa de Empreendedorismo, o objetivo do curso é capacitar e desenvolver o potencial de empreendedores, 
								contribuindo para a criação de startups que atendam a demandas da sociedade.</p>
								<p><b>As inscrições são gratuitas!</b></p>
								<p>Processo Seletivo:</p>
								<p>Além de preencher uma ficha de inscrição é necessário elaborar um texto e gravar um vídeo que contenham 
								algumas informações sobre a ideia de startup. A ideia deve buscar solucionar algum problema da sociedade 
								e de maneira eficaz e inovadora atingir o público que será beneficiado. As orientações para a inscrição 
								estão na página Maratona de Empreendedorismo da UFRGS.</p>
								<p>O curso será realizado na SEDETEC no segundo semestre de 2015, de 4 de agosto a 8 de dezembro, 
								com carga horária de 80 horas que contemplam encontros presenciais, atividades à distância, consultorias, 
								mentorias e o fechamento do projeto de startup.</p>
								<p>A programação completa e demais informações podem ser consultadas na página Maratona de Empreendedorismo da UFRGS.</p>
