								<h3><a id="2014-02-12">XV Entrepreneurship Marathon Enclosure</a></h3>
								<p>IECBiot team participated in this tuesday of the enclosure ceremony of the
								<a class="color1" target="_blank" href="http://www.ufrgs.br/empreendedorismo/maratona-de-empreendedorismo">
								XV Entrepreneurship Marathon</a> sponsored by SEDETEC through the Entrepreneurship and Innovation Program.
								The fifteenth edition of the marathon had the inscription of 30 projects of which 13 were classified as
								finalists</p>
								<p>The first place was the project named 3D Protos Saúde, authored by Eloisa Gonzaga, Fernando Flores and
								Vinicius Amantéa. They developed a process for orthosis and prosthesis based on body scanning and subsequent
								3D printing. The startup was born during the Marathon. Fernando and Vinicius began the couse with an idea,
								abandoned after they met Eloisa, who also left her inicial project aside to create the 3D Protos Saúde with
								her new colleagues. The second place was the startup Love Design Pet, Renata Moutinho, designing furniture for
								dogs and cats. The startup offers differentiate design of products that integrates the house decoration and is
								developed in environmental friendly materials. The third place went to Precision Base project from Jonatan Grandini
								and Luis Felipe Peixoto. They developed a product that combines hardware and software to evaluate how the surfer
								uses the board. From the analysis of the data captured by the software, manufactures can creat the ideal board
								for every type of customer.</p>
								<p>All finalists were awarded, the 3 finalists got special prizes. The winners won, among other awards, entrepreneurship
								courses and the formalization of companies. The ceremony was attended by the Vice Chancellor Rui Vicente Oppermann and 
								Technological Development Secretary Raquel Mauler, as well as members of SEDETEC and parthner institutions of the university.
								Oppermand said that encoraging entrepreneurship has a great social and economic significance, because, he said, "Brazil needs
								to be more efficient in transforming creativity in projects that promote life quality."</p>
								
